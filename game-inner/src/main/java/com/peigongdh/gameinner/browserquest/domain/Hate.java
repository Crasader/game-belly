package com.peigongdh.gameinner.browserquest.domain;

public class Hate {

    private int id;

    private int hate;

    public Hate(int id, int hate) {
        this.id = id;
        this.hate = hate;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getHate() {
        return hate;
    }

    public void setHate(int hate) {
        this.hate = hate;
    }
}
